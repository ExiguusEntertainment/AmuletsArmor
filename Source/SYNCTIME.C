#include "SYNCTIME.H"

/* The actual sync time value */
T_word32 G_syncTime = 1;

/****************************************************************************/
/*  Routine:  SyncTimeGet                                                   */
/****************************************************************************/
/*                                                                          */
/*  Description:                                                            */
/*                                                                          */
/*    Return the global sync time.                                          */
/*                                                                          */
/*                                                                          */
/*  Outputs:                                                                */
/*                                                                          */
/*    T_word32                    -- sync time                              */
/*                                                                          */
/*                                                                          */
/*  Revision History:                                                       */
/*                                                                          */
/*    Who  Date:     Comments:                                              */
/*    ---  --------  ---------                                              */
/*    LES  06/12/96  Created                                                */
/*                                                                          */
/****************************************************************************/

T_word32 SyncTimeGet(T_void)
{
    return G_syncTime ;
}

/****************************************************************************/
/*  Routine:  SyncTimeSet                                                   */
/****************************************************************************/
/*                                                                          */
/*  Description:                                                            */
/*                                                                          */
/*    Sets the global sync time.                                            */
/*                                                                          */
/*                                                                          */
/*  Inputs:                                                                 */
/*                                                                          */
/*    T_word32                    -- new sync time                          */
/*                                                                          */
/*                                                                          */
/*  Revision History:                                                       */
/*                                                                          */
/*    Who  Date:     Comments:                                              */
/*    ---  --------  ---------                                              */
/*    LES  06/12/96  Created                                                */
/*                                                                          */
/****************************************************************************/

T_void SyncTimeSet(T_word32 time)
{
    G_syncTime = time ;
}

